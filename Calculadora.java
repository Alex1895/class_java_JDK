package FundamentosJavaCore;

public class Calculadora {
		int sumar(int a, int b) {
			int res = a+b;
			System.out.println(res);
			return res;
		}
		int res(int a, int b) {
			int res = a-b;
			System.out.println(res);
			return res;
		}
		int mult(int a, int b) {
			int res = a*b;
			System.out.println(res);
			return res;
		}
		int div(int a, int b) {
			int res = a / b;
			System.out.println(res);
			return res;
		}
}
